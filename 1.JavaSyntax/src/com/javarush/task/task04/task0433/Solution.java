/* 
Гадание на долларовый счет
*/

public class Solution {
    public static void main(String[] args) {
        String s = "SSSSSSSSSS";
        int i = 1;
        while (i < 11) {
            System.out.println(s);
            i = i + 1;
        }
    }
}
